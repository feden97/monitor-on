function parseDate(dateStr) {
  if (!dateStr || dateStr.trim() === '') return null
  const parts = dateStr.trim().split('/')
  if (parts.length === 3) {
    return `${parts[2]}-${parts[1]}-${parts[0]}`
  }
  return dateStr
}

let prospectos = {}
if (fs.existsSync(DATA_PATH)) {
  const fileData = fs.readFileSync(DATA_PATH, 'utf-8')
  prospectos = JSON.parse(fileData)
}

const lines = rawData.trim().split('\n')
for (const line of lines) {
  const parts = line.split('\t')
  if (parts.length >= 10) {
    const name = parts[0].trim()
    const tickerD = parts[1].trim()
    const tickerO = parts[2].trim()
    const couponStr = parts[3].replace('%', '').replace(',', '.').trim()
    const freqStr = parts[4].trim()
    const freq = freqStr ? parseInt(freqStr, 10) : null
    const maturity = parseDate(parts[5].trim())
    const amortType = parts[6].trim()
    const minInvStr = parts[7].trim()
    const minInv = minInvStr ? parseInt(minInvStr, 10) : 1
    const law = parts[8].trim()
    const dollar = parts[9].trim()
    const rating = parts.length > 10 ? parts[10].trim() : ''

    if (!prospectos[tickerD]) {
      prospectos[tickerD] = {}
    }

    const p = prospectos[tickerD]
    p.name = name
    p.ticker_o = tickerO
    p.coupon_rate = couponStr ? parseFloat(couponStr) : 0
    p.frequency = freq
    p.maturity_date = maturity
    p.amort_type = amortType
    p.min_investment = minInv
    p.law = law
    p.currency_type = dollar
    if (rating) p.rating = rating
  }
}

fs.writeFileSync(DATA_PATH, JSON.stringify(prospectos, null, 2), 'utf-8')
console.log(`✅ Updated ${Object.keys(prospectos).length} bonds in ${DATA_PATH}`)
